@RunWith(SpringRunner.class)
@SpringBootTest
@AutoConfigureMockMvc
class AbcProjectApplicationTests{
    @Autowired
    private MockMvc mvc;

    @Autowired
    private FlightDetailsRepository repository;

	@Test
	void contextLoads() {
	}

	@Test
	public void getFlights() throws Exception 
	{
		SimpleDateFormat sf = new SimpleDateFormat("yyyy-MM-dd");

	    mvc.perform(get("/flights")
	      .contentType(MediaType.APPLICATION_JSON))
	      .andExpect(status().isOk())
	      .andExpect(content()
	      .contentTypeCompatibleWith(MediaType.APPLICATION_JSON))
	      .andExpect(jsonPath("$[0].arrivalTime", startsWith("2021-08-06T10:50:00")));
	}
	
	@Test
	public void getFlightsForAirline() throws Exception 
	{
		mvc.perform(get("/flights/EK")
	      .contentType(MediaType.APPLICATION_JSON))
	      .andExpect(status().isOk())
	      .andExpect(content()
	      .contentTypeCompatibleWith(MediaType.APPLICATION_JSON))
	      .andExpect(jsonPath("$[0].flightNumber", startsWith("EK")));
	}
}
